'''
## 7. Нули в конец

В школе прошёл экзамен по математике. Несколько человек списали решения и получили 0 баллов.
Дан массив оценок, среди которых есть 0.
Необходимо перенести все нули в конец массива, чтобы такие ученики оказались в конце списка.
Порядок остальных элементов должен сохраняться.

**Что нужно сделать:**
Реализуйте алгоритм, который за один проход переносит все нулевые элементы вперёд.
Поясните, почему ваше решение является стабильным и работает за O(n).

**Решение:**
Один проход слева направо с указателем l — позицией для следующего ненулевого элемента.
Все ненулевые элементы последовательно записываются в начало массива, затем оставшаяся часть заполняется нулями.

Работает за O(n) — один проход по массиву.
Стабильно, так как элементы записываются в порядке их появления.
'''

class Solution:
    def move_zeros(self, nums):
        l = 0

        for i in range(len(nums)):
            if nums[i] != 0:
                if i != l:
                    nums[l] = nums[i]
                l += 1
        
        for i in range(l, len(nums)):
            nums[i] = 0
